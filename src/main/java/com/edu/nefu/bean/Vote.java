package com.edu.nefu.bean;

public class Vote {
    public int[] ticket;
    public int length;

    public Vote() {
    }

    public int[] getTicket() {
        return ticket;
    }

    public void setTicket(int[] ticket) {
        this.ticket = ticket;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
